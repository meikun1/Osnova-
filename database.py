def dl_get(bot_id: int) -> dict | None:
    with _lock:
        row = _db.one(
            "SELECT enabled, startapp_token, token_version "
            "FROM direct_link_bots WHERE bot_id=?",
            (bot_id,),
        )
    if row is None:
        return None
    return {
        "enabled": bool(row["enabled"]),
        "startapp_token": row["startapp_token"],
        "token_version": row["token_version"],
    }

def dl_init(bot_id: int, startapp_token: str) -> dict:
    now = _now()
    with _lock:
        _db.execute(
            "INSERT INTO direct_link_bots(bot_id, enabled, startapp_token, "
            "token_version, created_at, updated_at) VALUES(?,0,?,1,?,?) "
            "ON CONFLICT(bot_id) DO NOTHING",
            (bot_id, startapp_token, now, now),
        )
        _db.commit()
    state = dl_get(bot_id)
    assert state is not None
    return state

def dl_set_enabled(bot_id: int, enabled: bool) -> None:
    with _lock:
        _db.execute(
            "UPDATE direct_link_bots SET enabled=?, updated_at=? WHERE bot_id=?",
            (1 if enabled else 0, _now(), bot_id),
        )
        _db.commit()

def dl_rotate(bot_id: int, new_token: str) -> dict:
    with _lock:
        _db.execute(
            "UPDATE direct_link_bots SET startapp_token=?, "
            "token_version=token_version+1, updated_at=? WHERE bot_id=?",
            (new_token, _now(), bot_id),
        )
        _db.commit()
    state = dl_get(bot_id)
    assert state is not None
    return state


# ===== CF account pool =====

def cf_pool_add(email: str | None, api_token: str, label: str | None = None) -> bool:
    """Добавить аккаунт в пул. Возвращает True если добавлен, False если дубль
    или токен невалиден (не-ASCII)."""
    api_token = (api_token or "").strip()
    if not api_token or not api_token.isascii():
        return False
    with _lock:
        row = _db.one(
            "SELECT id FROM cf_accounts WHERE api_token=?", (api_token,)
        )
        if row:
            return False
        _db.execute(
            "INSERT INTO cf_accounts(email, api_token, label, created_at) "
            "VALUES(?,?,?,?)",
            (email, api_token, label, _now()),
        )
        _db.commit()
    return True


def cf_pool_stats() -> dict:
    with _lock:
        total = _db.one("SELECT COUNT(*) AS c FROM cf_accounts")["c"]
        free = _db.one(
            "SELECT COUNT(*) AS c FROM cf_accounts WHERE user_id IS NULL"
        )["c"]
    return {"total": int(total), "free": int(free), "taken": int(total) - int(free)}


def cf_pool_get_for_user(user_id: int) -> dict | None:
    """Возвращает уже привязанный к юзеру аккаунт или атомарно клеймит
    первый свободный. None — если пул пуст."""
    with _lock:
        row = _db.one(
            "SELECT id, email, api_token, label FROM cf_accounts WHERE user_id=? "
            "ORDER BY id LIMIT 1",
            (user_id,),
        )
        if row:
            return dict(row)

        if _db.kind == "pg":
            row = _db.one(
                "UPDATE cf_accounts SET user_id=?, assigned_at=? WHERE id=("
                " SELECT id FROM cf_accounts WHERE user_id IS NULL "
                " ORDER BY id LIMIT 1 FOR UPDATE SKIP LOCKED"
                ") RETURNING id, email, api_token, label",
                (user_id, _now()),
            )
        else:
            free = _db.one(
                "SELECT id FROM cf_accounts WHERE user_id IS NULL "
                "ORDER BY id LIMIT 1"
            )
            if not free:
                return None
            _db.execute(
                "UPDATE cf_accounts SET user_id=?, assigned_at=? WHERE id=?",
                (user_id, _now(), free["id"]),
            )
            row = _db.one(
                "SELECT id, email, api_token, label FROM cf_accounts WHERE id=?",
                (free["id"],),
            )
        _db.commit()
        return dict(row) if row else None


def cf_pool_release(account_id: int) -> None:
    """Освободить аккаунт (админская операция). Связанные user_domains
    остаются как есть — это только маркер 'аккаунт снова доступен'."""
    with _lock:
        _db.execute(
            "UPDATE cf_accounts SET user_id=NULL, assigned_at=NULL WHERE id=?",
            (account_id,),
        )
        _db.commit()


