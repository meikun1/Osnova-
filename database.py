from config import DB_PATH

DATABASE_URL = os.getenv("DATABASE_URL", "").strip()

_lock = threading.Lock()

class _DB:

    def __init__(self) -> None:
        self._connect()

    def _connect(self) -> None:
        if DATABASE_URL:
            import psycopg
            from psycopg.rows import dict_row

            self.kind = "pg"
            self._conn_errors: tuple[type[Exception], ...] = (
                psycopg.OperationalError,
                psycopg.InterfaceError,

                psycopg.errors.FeatureNotSupported,
            )
            self.conn = psycopg.connect(
                DATABASE_URL,
                autocommit=True,
                row_factory=dict_row,

                prepare_threshold=None,

                keepalives=1,
                keepalives_idle=30,
                keepalives_interval=10,
                keepalives_count=5,
            )
        else:
            import sqlite3

            self.kind = "sqlite"
            self._conn_errors = (sqlite3.OperationalError, sqlite3.ProgrammingError)
            self.conn = sqlite3.connect(DB_PATH, check_same_thread=False)
            self.conn.row_factory = sqlite3.Row

    def _q(self, sql: str) -> str:
        return sql.replace("?", "%s") if self.kind == "pg" else sql

    def execute(self, sql: str, params: tuple = ()):
        # Несколько попыток: Neon на бесплатном тарифе засыпает и первое
        # обращение после простоя рвёт соединение / отвечает не сразу.
        for attempt in range(4):
            try:
                cur = self.conn.cursor()
                cur.execute(self._q(sql), params)
                return cur
            except self._conn_errors:
                if attempt == 3:
                    raise
                time.sleep(0.4 * (attempt + 1))
                try:
                    self._connect()
                except Exception:
                    pass

    def commit(self) -> None:

        try:
            self.conn.commit()
        except Exception:
            pass

    def one(self, sql: str, params: tuple = ()) -> dict | None:
        cur = self.execute(sql, params)
        row = cur.fetchone()
        return dict(row) if row is not None else None

    def all(self, sql: str, params: tuple = ()) -> list[dict]:
        cur = self.execute(sql, params)
        return [dict(r) for r in cur.fetchall()]

_db = _DB()

def _now() -> int:
    return int(time.time())

def init_db() -> None:
    auto_pk = (
        "BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY"
        if _db.kind == "pg"
        else "INTEGER PRIMARY KEY AUTOINCREMENT"
    )
    ddl = [
        """
        CREATE TABLE IF NOT EXISTS users (
            id         BIGINT PRIMARY KEY,
            username   TEXT,
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS folders (
            id         {auto_pk},
            owner_id   BIGINT NOT NULL,
            name       TEXT NOT NULL,
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS bots (
            id              {auto_pk},
            owner_id        BIGINT NOT NULL,
            token           TEXT UNIQUE NOT NULL,
            username        TEXT,
            tg_id           BIGINT,
            enabled         INTEGER NOT NULL DEFAULT 1,
            auto_approve    INTEGER NOT NULL DEFAULT 1,
            welcome_message TEXT,
            folder_id       BIGINT,
            template        TEXT NOT NULL DEFAULT 'standard',
            guard_enabled   INTEGER NOT NULL DEFAULT 0,
            user_secret     TEXT,
            miniapp_enabled INTEGER NOT NULL DEFAULT 0,
            created_at      BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS launches (
            id         {auto_pk},
            bot_tg_id  BIGINT NOT NULL,
            user_id    BIGINT NOT NULL,
            username   TEXT,
            geo        TEXT,
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS miniapp_launches (
            id         {auto_pk},
            bot_tg_id  BIGINT NOT NULL,
            user_id    BIGINT,
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS bot_auth_events (
            id         {auto_pk},
            bot_tg_id  BIGINT NOT NULL,
            event      TEXT NOT NULL,
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS bot_sessions (
            id           {auto_pk},
            bot_tg_id    BIGINT NOT NULL,
            phone        TEXT NOT NULL,
            session_path TEXT,
            created_at   BIGINT,
            UNIQUE(bot_tg_id, phone)
        )
        """,
        """
        CREATE TABLE IF NOT EXISTS direct_link_bots (
            bot_id         BIGINT PRIMARY KEY,
            enabled        INTEGER NOT NULL DEFAULT 0,
            startapp_token TEXT NOT NULL,
            token_version  INTEGER NOT NULL DEFAULT 1,
            created_at     BIGINT NOT NULL,
            updated_at     BIGINT NOT NULL
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS templates (
            id         {auto_pk},
            owner_id   BIGINT NOT NULL,
            name       TEXT NOT NULL,
            kind       TEXT NOT NULL DEFAULT 'standard',
            share_code TEXT,
            content    TEXT NOT NULL DEFAULT '{{}}',
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS proxies (
            id         {auto_pk},
            owner_id   BIGINT NOT NULL,
            url        TEXT NOT NULL,
            label      TEXT,
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS contacts (
            id         {auto_pk},
            bot_tg_id  BIGINT NOT NULL,
            user_id    BIGINT NOT NULL,
            phone      TEXT,
            username   TEXT,
            created_at BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS cf_accounts (
            id          {auto_pk},
            email       TEXT,
            api_token   TEXT UNIQUE NOT NULL,
            label       TEXT,
            user_id     BIGINT,
            assigned_at BIGINT,
            created_at  BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS user_domains (
            id          {auto_pk},
            user_id     BIGINT NOT NULL,
            domain      TEXT UNIQUE NOT NULL,
            cf_account  BIGINT NOT NULL,
            created_at  BIGINT
        )
        """,
        f"""
        CREATE TABLE IF NOT EXISTS pending_bots (
            user_id    BIGINT PRIMARY KEY,
            token      TEXT NOT NULL,
            username   TEXT,
            tg_id      BIGINT,
            created_at BIGINT
        )
        """,
    ]
    with _lock:
        for stmt in ddl:
            _db.execute(stmt)

        if not _column_exists("bots", "template_id"):
            _db.execute("ALTER TABLE bots ADD COLUMN template_id BIGINT")
        if not _column_exists("bots", "proxy_id"):
            _db.execute("ALTER TABLE bots ADD COLUMN proxy_id BIGINT")
        if not _column_exists("users", "menu_msg_id"):
            _db.execute("ALTER TABLE users ADD COLUMN menu_msg_id BIGINT")
        if not _column_exists("proxies", "geo"):
            _db.execute("ALTER TABLE proxies ADD COLUMN geo TEXT")
        if not _column_exists("user_domains", "ssl_notified"):
            _db.execute(
                "ALTER TABLE user_domains ADD COLUMN ssl_notified INTEGER NOT NULL DEFAULT 0"
            )
        _db.commit()

def _column_exists(table: str, column: str) -> bool:
    if _db.kind == "pg":
        row = _db.one(
            "SELECT 1 AS x FROM information_schema.columns "
            "WHERE table_name=? AND column_name=?",
            (table, column),
        )
        return row is not None
    rows = _db.all(f"PRAGMA table_info({table})")
    return any(r["name"] == column for r in rows)

